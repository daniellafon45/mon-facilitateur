import {
  AlertTriangle,
  BarChart3,
  Calendar,
  CheckSquare,
  Clipboard,
  Columns,
  Eye,
  FileText,
  Globe,
  Grid3X3,
  Heart,
  HelpCircle,
  Layers,
  Layout,
  Lightbulb,
  Link2,
  List,
  Pencil,
  RefreshCw,
  Route,
  SlidersHorizontal,
  Sparkles,
  Target,
  Users,
  type LucideIcon,
} from "lucide-react";

const MAP: Record<string, LucideIcon> = {
  Sparkles,
  Sliders: SlidersHorizontal,
  Target,
  Link: Link2,
  Grid: Grid3X3,
  List,
  Eye,
  FileText,
  HelpCircle,
  Globe,
  Pencil,
  Users,
  Lightbulb,
  Heart,
  AlertTriangle,
  BarChart: BarChart3,
  Clipboard,
  Layers,
  User: Users,
  Route,
  RefreshCw,
  CheckSquare,
  Columns,
  Layout,
  Calendar,
};

export function resolveMethodIcon(name?: string): LucideIcon {
  if (!name) return Grid3X3;
  return MAP[name] ?? Grid3X3;
}
